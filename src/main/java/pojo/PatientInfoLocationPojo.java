package pojo;

public class PatientInfoLocationPojo {

        private byte[] blockHash;
        private byte[] patientInfoHash;

        public byte[] getBlockHash() {
            return blockHash;
        }

        public void setBlockHash(byte[] blockHash) {
            this.blockHash = blockHash;
        }

        public byte[] getPatientInfoHash() {
            return patientInfoHash;
        }

        public void setPatientInfoHash(byte[] patientInfoHash) {
            this.patientInfoHash = patientInfoHash;
        }


}
